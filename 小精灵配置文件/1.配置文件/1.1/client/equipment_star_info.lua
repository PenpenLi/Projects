    return #equipment_star_info._data
end

function equipment_star_info.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

function equipment_star_info.indexOf(index)
    if index == nil then
        return nil
    end
    return setmetatable({_raw = equipment_star_info._data[index]}, m)
end

