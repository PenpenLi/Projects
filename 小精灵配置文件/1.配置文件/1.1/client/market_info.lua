local m = {
    __index = function(t,k)
        if k == "toObject"then
            return function()
                local o = {}
                for key, v in pairs (__key_map) do
                    o[key] = t._raw[v]
                end
                return o
            end
        end

        assert(__key_map[k], "cannot find " .. k .. " in record_market_info")
        return t._raw[__key_map[k]]
    end
}

function market_info.getLength()
    return #market_info._data
end

function market_info.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

function market_info.indexOf(index)
    if index == nil then
        return nil
    end
    return setmetatable({_raw = market_info._data[index]}, m)
end

function market_info.get(id)
    local k = id
    return market_info.indexOf(__index_id[k])
end

