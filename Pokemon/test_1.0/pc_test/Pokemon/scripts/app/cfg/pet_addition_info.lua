    local k = id .. '_'.. level
    return pet_addition_info.indexOf(__index_id_level[k])
end

function pet_addition_info.set(id,level, key, value)
    local record = pet_addition_info.get(id,level)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

function pet_addition_info.get_index_data()
    return __index_id_level
end

