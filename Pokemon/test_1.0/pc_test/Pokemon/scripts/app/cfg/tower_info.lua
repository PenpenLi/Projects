    return #tower_info._data
end

function tower_info.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

function tower_info.indexOf(index)
    if index == nil then
        return nil
    end
    return setmetatable({_raw = tower_info._data[index]}, m)
end

