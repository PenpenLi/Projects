    error_msg = 2,
}

local m = {
    __index = function(t,k)
        if k == "toObject"then
            return function()
                local o = {}
                for key, v in pairs (__key_map) do
                    o[key] = t._raw[v]
                end
                return o
            end
        end

        assert(__key_map[k], "cannot find " .. k .. " in record_net_msg_error")
        return t._raw[__key_map[k]]
    end
}

function net_msg_error.getLength()
    return #net_msg_error._data
end

function net_msg_error.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

function net_msg_error.indexOf(index)
    if index == nil then
        return nil
    end
    return setmetatable({_raw = net_msg_error._data[index]}, m)
end

function net_msg_error.get(error_id)
    local k = error_id
    return net_msg_error.indexOf(__index_error_id[k])
end

function net_msg_error.set(error_id, key, value)
    local record = net_msg_error.get(error_id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

function net_msg_error.get_index_data()
    return __index_error_id
end

