local m = {
    __index = function(t,k)
        if k == "toObject"then
            return function()
                local o = {}
                for key, v in pairs (__key_map) do
                    o[key] = t._raw[v]
                end
                return o
            end
        end

        assert(__key_map[k], "cannot find " .. k .. " in record_play_info")
        return t._raw[__key_map[k]]
    end
}

function play_info.getLength()
    return #play_info._data
end

function play_info.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

function play_info.indexOf(index)
    if index == nil then
        return nil
    end
    return setmetatable({_raw = play_info._data[index]}, m)
end

function play_info.get(play_group_id,skill_id)
    local k = play_group_id .. '_'.. skill_id
    return play_info.indexOf(__index_play_group_id_skill_id[k])
end

function play_info.set(play_group_id,skill_id, key, value)
    local record = play_info.get(play_group_id,skill_id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

function play_info.get_index_data()
    return __index_play_group_id_skill_id
