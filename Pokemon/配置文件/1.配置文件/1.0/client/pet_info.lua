local m = {
    __index = function(t,k)
        if k == "toObject"then
            return function()
                local o = {}
                for key, v in pairs (__key_map) do
                    o[key] = t._raw[v]
                end
                return o
            end
        end

        assert(__key_map[k], "cannot find " .. k .. " in record_pet_info")
        return t._raw[__key_map[k]]
    end
}

function pet_info.getLength()
    return #pet_info._data
end

function pet_info.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

function pet_info.indexOf(index)
    if index == nil then
        return nil
    end
    return setmetatable({_raw = pet_info._data[index]}, m)
end

function pet_info.get(id)
    local k = id
    return pet_info.indexOf(__index_id[k])
end

function pet_info.set(id, key, value)
    local record = pet_info.get(id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

function pet_info.get_index_data()
    return __index_id
