local m = {
    __index = function(t,k)
        if k == "toObject"then
            return function()
                local o = {}
                for key, v in pairs (__key_map) do
                    o[key] = t._raw[v]
                end
                return o
            end
        end

        assert(__key_map[k], "cannot find " .. k .. " in record_handbook_info")
        return t._raw[__key_map[k]]
    end
}

function handbook_info.getLength()
    return #handbook_info._data
end

function handbook_info.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

function handbook_info.indexOf(index)
    if index == nil then
        return nil
    end
    return setmetatable({_raw = handbook_info._data[index]}, m)
end

function handbook_info.get(id)
    local k = id
    return handbook_info.indexOf(__index_id[k])
end

function handbook_info.set(id, key, value)
    local record = handbook_info.get(id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

function handbook_info.get_index_data()
    return __index_id
end

